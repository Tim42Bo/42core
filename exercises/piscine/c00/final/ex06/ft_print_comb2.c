#include <unistd.h>

void	print(char c)

{
	write(1, &c, 1);
}

void	ft_print_comb2(void)

{
	int	x;
	int	y;

	x = 0;
	while (x <= 98)
	{
		y = x +1;
		while (y <= 99)
		{
			print(48 + x / 10);
			print(48 + x % 10);
			print(' ');
			print(48 + y / 10);
			print(48 + y % 10);
			if (x != 98)
				write (1, ", ", 2);
			y++;
		}
		x++;
	}
}
